
public class Ex008 {

	public static void main(String[] args) {
		// 8. Operadores de Incremento
		// Declare uma variável contador com valor inicial de 5. Use o operador de incremento ++ para
		// aumentar o valor e exiba o novo valor. Em seguida, use o operador de decremento -- para
		// diminuir o valor e mostre o resultado
		int numero = 5;
		numero++;
		System.out.println("numero = " + numero);
		numero--;
		System.out.println("numero = " + numero);
		

	}

}
